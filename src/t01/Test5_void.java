package t01;

public class Test5_void { // 실행할 수 있는 클래스만 main 메소드가 있으면 된다
	public static void main(String[] args) {
		/* 
		- void 리턴 타입(반환). 리턴할 값이 없을 때 사용
		- String(문자열 타입) [](배열)

		- ctrl + d 열 삭제
		- ctrl + alt + 방향키 열 복사
		- alt + 방향키 열 이동 
		*/
		System.out.println("1.반갑\t습니다."); // \t tab 사이즈만큼 띄움
		System.out.print("2.반갑습니다. \n\n"); // \n 줄바꿈
		System.out.println("3.반갑습니다.");
		System.out.println("4.반갑습니다.");
	} // <- 메소드 블럭

}
